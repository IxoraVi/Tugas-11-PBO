    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        if (bedit.getText() == "bedit"){
            txtnomhs.requestFocus();
        } else {
            String Nomhs = txtnomhs.getText();
            String Nama = txtnama.getText();
            String Jurusan = (String) cmbjur.getSelectedItem();
            
            try {
                Statement statement = (Statement)koneksi.getConnection().createStatement();
                statement.executeUpdate("update tabel_mahasiswa SET Nomhs='"+Nomhs+"',"+"Nama='"+Nama+"',"+"Jurusan='"+Jurusan+"'" + "WHERE Nomhs='"+Nomhs+"' OR Nama='"+Nama+"'");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Behasil Diubah");
            } catch (Exception t) {
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah");
            }
            datatable();
        }
    }//GEN-LAST:event_beditActionPerformed

    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String Nomhs = txtnomhs.getText();
        String Nama = txtnama.getText();
        String Jurusan = (String) cmbjur.getSelectedItem();
        try {
            Statement statement = (Statement)koneksi.getConnection().createStatement();
            statement.executeUpdate("insert into tabel_mahasiswa VALUES ('"+ Nomhs +"','"+ Nama +"','"+ Jurusan +"');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Behasil Disimpan");
        } catch (Exception t) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
        }
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        String Nomhs = txtnomhs.getText();
        String Nama = txtnama.getText();
        try {
            Statement statement = (Statement)koneksi.getConnection().createStatement();
            statement.executeUpdate("delete from tabel_mahasiswa where Nomhs='"+Nomhs+"' OR Nama='"+Nama+"';");
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
            txtnomhs.setText("");
            txtnama.setText("");
            cmbjur.setSelectedItem("Teknik Informatika");
        } catch (Exception t){
            JOptionPane.showMessageDialog(null, "Data Gagal Dihapus");
        }
        datatable();
    }//GEN-LAST:event_bhapusActionPerformed

