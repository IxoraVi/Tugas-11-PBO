    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
        try {
            Statement statement = (Statement)koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("Select*from tabel_mahasiswa where "+"Nomhs like '%"+txtcari.getText()+"%' OR "+"Nama like '%"+txtcari.getText()+"%'");
            DefaultTableModel tbl = new DefaultTableModel();
            tbl.addColumn("Nomhs");
            tbl.addColumn("Nama");
            tbl.addColumn("Jurusan");

            tabel.setModel(tbl);
            while (res.next()){
                tbl.addRow(new Object[] {
                    res.getString ("Nomhs"),
                    res.getString ("Nama"),
                    res.getString ("Jurusan"),
                });
                tabel.setModel(tbl);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
    }//GEN-LAST:event_bcariActionPerformed

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int a = tabel.getSelectedRow();
        if (a == -1){
            return;
        }
        String Nomhs = (String) tabel.getValueAt(a, 0);
        txtnomhs.setText(Nomhs);
        String Nama = (String) tabel.getValueAt(a, 1);
        txtnama.setText(Nama);
        String Jurusan = (String) tabel.getValueAt(a, 2);
        cmbjur.setSelectedItem(Jurusan);
    }//GEN-LAST:event_tabelMouseClicked

